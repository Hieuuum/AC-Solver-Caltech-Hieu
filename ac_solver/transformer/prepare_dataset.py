"""
Post-processes the raw dataset generated by data_generator.py into tokenized
train/val splits suitable for transformer training.

Takes the merged presentations.npy and metadata.npy files and produces:
  - train.npy: tokenized training sequences (90%)
  - val.npy: tokenized validation sequences (10%)
  - dataset_info.json: statistics (counts, GS-solved/unsolved breakdown, etc.)

Each row in the output arrays is a variable-length token sequence padded to
a fixed length. The token vocabulary is defined in tokenizer.py (6 tokens).

Usage:
    python -m ac_solver.transformer.prepare_dataset --input-dir data/transformer_dataset

    # Custom split ratio and context length
    python -m ac_solver.transformer.prepare_dataset --input-dir data/transformer_dataset \\
        --val-fraction 0.1 --context-length 1024
"""

import argparse
import json
import os
from ast import literal_eval

import numpy as np
from importlib import resources

from ac_solver.transformer.tokenizer import presentation_to_tokens, TOKEN_EOS


def load_gs_solved_indices():
    """Load the set of indices (into all_presentations.txt) that are GS-solved.

    Returns:
        set of int: indices of GS-solved presentations in all_presentations.txt
    """
    # Load all presentations
    with resources.open_text(
        "ac_solver.search.miller_schupp.data", "all_presentations.txt"
    ) as f:
        all_pres = [line.strip() for line in f]

    # Load GS-solved presentations
    with resources.open_text(
        "ac_solver.search.miller_schupp.data", "greedy_solved_presentations.txt"
    ) as f:
        gs_solved = {line.strip() for line in f}

    # Find indices of GS-solved presentations
    solved_indices = set()
    for idx, pres_str in enumerate(all_pres):
        if pres_str in gs_solved:
            solved_indices.add(idx)

    return solved_indices


def tokenize_presentations(presentations, lmax):
    """Convert a batch of numpy presentations to token sequences.

    Parameters:
        presentations: numpy array of shape (N, 2 * lmax), dtype int8
        lmax: max relator length used in the presentation layout

    Returns:
        list of lists of ints (token sequences, variable length)
    """
    token_sequences = []
    for i in range(len(presentations)):
        tokens = presentation_to_tokens(presentations[i], max_relator_length=lmax)
        token_sequences.append(tokens)
    return token_sequences


def pad_sequences(sequences, context_length, pad_token=TOKEN_EOS):
    """Pad token sequences to a fixed length, truncating if necessary.

    Parameters:
        sequences: list of lists of ints
        context_length: target sequence length
        pad_token: token to use for padding (default: EOS)

    Returns:
        numpy array of shape (N, context_length), dtype int16
    """
    n = len(sequences)
    padded = np.full((n, context_length), pad_token, dtype=np.int16)
    for i, seq in enumerate(sequences):
        length = min(len(seq), context_length)
        padded[i, :length] = seq[:length]
    return padded


def prepare_dataset(args):
    """Main entry point: tokenize and split the generated dataset."""
    input_dir = args.input_dir

    # Load merged arrays
    pres_path = os.path.join(input_dir, "presentations.npy")
    meta_path = os.path.join(input_dir, "metadata.npy")

    if not os.path.exists(pres_path):
        raise FileNotFoundError(
            f"presentations.npy not found in {input_dir}. "
            "Run data_generator.py first to generate the raw dataset."
        )

    print("Loading presentations...")
    presentations = np.load(pres_path)
    metadata = np.load(meta_path)
    n_total = len(presentations)
    lmax = presentations.shape[1] // 2

    print(f"  Total presentations: {n_total:,}")
    print(f"  Max relator length: {lmax}")

    # Load GS-solved indices for tagging
    print("Loading GS-solved labels...")
    gs_solved_indices = load_gs_solved_indices()
    print(f"  GS-solved seed presentations: {len(gs_solved_indices)}")

    # Tag each presentation with its GS-solved/unsolved label
    origin_indices = metadata[:, 0]  # which of the 1190 MS presentations it came from
    is_gs_solved = np.array([idx in gs_solved_indices for idx in origin_indices])
    n_from_solved = int(is_gs_solved.sum())
    n_from_unsolved = n_total - n_from_solved

    print(f"  Presentations from GS-solved seeds: {n_from_solved:,}")
    print(f"  Presentations from GS-unsolved seeds: {n_from_unsolved:,}")

    # Tokenize
    print("Tokenizing presentations...")
    token_sequences = tokenize_presentations(presentations, lmax)

    # Compute sequence length statistics
    lengths = [len(seq) for seq in token_sequences]
    print(f"  Token sequence lengths: min={min(lengths)}, "
          f"max={max(lengths)}, mean={np.mean(lengths):.1f}")
    total_tokens = sum(lengths)
    print(f"  Total tokens: {total_tokens:,}")

    # Pad to context length
    context_length = args.context_length
    n_truncated = sum(1 for l in lengths if l > context_length)
    if n_truncated > 0:
        print(f"  WARNING: {n_truncated} sequences will be truncated "
              f"to context length {context_length}")

    print(f"Padding to context length {context_length}...")
    padded = pad_sequences(token_sequences, context_length)

    # Shuffle and split
    print("Shuffling and splitting...")
    rng = np.random.default_rng(args.seed)
    shuffle_idx = rng.permutation(n_total)
    padded = padded[shuffle_idx]
    is_gs_solved = is_gs_solved[shuffle_idx]
    shuffled_metadata = metadata[shuffle_idx]

    n_val = int(n_total * args.val_fraction)
    n_train = n_total - n_val

    train_tokens = padded[:n_train]
    val_tokens = padded[n_train:]
    train_gs_labels = is_gs_solved[:n_train]
    val_gs_labels = is_gs_solved[n_train:]
    train_metadata = shuffled_metadata[:n_train]
    val_metadata = shuffled_metadata[n_train:]

    print(f"  Train: {n_train:,} sequences")
    print(f"  Val: {n_val:,} sequences")

    # Save
    output_dir = args.output_dir or input_dir
    os.makedirs(output_dir, exist_ok=True)

    train_path = os.path.join(output_dir, "train.npy")
    val_path = os.path.join(output_dir, "val.npy")
    train_labels_path = os.path.join(output_dir, "train_gs_labels.npy")
    val_labels_path = os.path.join(output_dir, "val_gs_labels.npy")
    train_meta_path = os.path.join(output_dir, "train_metadata.npy")
    val_meta_path = os.path.join(output_dir, "val_metadata.npy")

    print(f"Saving to {output_dir}...")
    np.save(train_path, train_tokens)
    np.save(val_path, val_tokens)
    np.save(train_labels_path, train_gs_labels)
    np.save(val_labels_path, val_gs_labels)
    np.save(train_meta_path, train_metadata)
    np.save(val_meta_path, val_metadata)

    # Save info
    info = {
        "n_total": n_total,
        "n_train": n_train,
        "n_val": n_val,
        "val_fraction": args.val_fraction,
        "context_length": context_length,
        "lmax": lmax,
        "seed": args.seed,
        "total_tokens": total_tokens,
        "mean_sequence_length": float(np.mean(lengths)),
        "max_sequence_length": max(lengths),
        "min_sequence_length": min(lengths),
        "n_from_gs_solved_seeds": n_from_solved,
        "n_from_gs_unsolved_seeds": n_from_unsolved,
        "n_truncated": n_truncated,
        "vocab_size": 6,
        "files": {
            "train_tokens": "train.npy",
            "val_tokens": "val.npy",
            "train_gs_labels": "train_gs_labels.npy",
            "val_gs_labels": "val_gs_labels.npy",
            "train_metadata": "train_metadata.npy",
            "val_metadata": "val_metadata.npy",
        },
    }

    info_path = os.path.join(output_dir, "dataset_info.json")
    with open(info_path, "w") as f:
        json.dump(info, f, indent=2)
    print(f"Saved dataset info to {info_path}")

    # Print summary
    print("\n--- Dataset Summary ---")
    print(f"  Train: {n_train:,} sequences, shape {train_tokens.shape}")
    print(f"  Val: {n_val:,} sequences, shape {val_tokens.shape}")
    train_size_mb = os.path.getsize(train_path) / (1024 * 1024)
    val_size_mb = os.path.getsize(val_path) / (1024 * 1024)
    print(f"  Train file: {train_size_mb:.1f} MB")
    print(f"  Val file: {val_size_mb:.1f} MB")
    print(f"  Total tokens: {total_tokens:,}")


def parse_args():
    parser = argparse.ArgumentParser(
        description="Tokenize and split the generated transformer dataset"
    )
    parser.add_argument(
        "--input-dir",
        type=str,
        default="data/transformer_dataset",
        help="Directory containing presentations.npy and metadata.npy",
    )
    parser.add_argument(
        "--output-dir",
        type=str,
        default=None,
        help="Output directory (default: same as input-dir)",
    )
    parser.add_argument(
        "--val-fraction",
        type=float,
        default=0.1,
        help="Fraction of data for validation (default: 0.1)",
    )
    parser.add_argument(
        "--context-length",
        type=int,
        default=1024,
        help="Max sequence length / context window (default: 1024)",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=42,
        help="Random seed for shuffling (default: 42)",
    )
    return parser.parse_args()


if __name__ == "__main__":
    args = parse_args()
    prepare_dataset(args)
